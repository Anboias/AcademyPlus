#include "libft.h"

int		ft_strtrim_end(char const *s)
{
	int		len;

	len = ft_strlen(s) - 1;
	while (s[len] == ' ' || s[len] == '\n' || s[len] == '\t')
	{
		len--;
	}
	return (len);
}
