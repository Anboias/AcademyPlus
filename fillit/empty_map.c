#include "fillheader.h"

/*
** Initialize content of empty map to '.'
*/

void	empty_map(char map[][15])
{
	int i;
	int j;

	i = 0;
	while (i < 15)
	{
		j = 0;
		while (j < 15)
		{
			map[i][j] = '.';
			j++;
		}
		i++;
	}
}
