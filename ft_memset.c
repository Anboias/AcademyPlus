#include "libft.h"
#include <stdio.h>

void	*ft_memset(void *s, int c, size_t n)
{
	t_byte	*t;

	t = (t_byte *)s;
	while (n--)
	{
		*t++ = (t_byte)c;
	}
	return (s);
}
