#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	if (strlen(s1) != strlen(s2))
		return (0);
	while (*s1)
	{
		if (*s1++ != *s2++)
			return (0);
	}
	return (1);
}
