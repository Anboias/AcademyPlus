char	*ft_strcpy(char *dest, char const *src)
{
	char	*p;

	if (!dest || !src)
	{
		return (dest);
	}
	p = (char *)dest;
	while (*src)
	{
		*dest++ = *src++;
	}
	*dest = '\0';
	return (p);
}
